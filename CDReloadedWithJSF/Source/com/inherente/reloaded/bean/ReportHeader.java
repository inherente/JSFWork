package com.inherente.reloaded.bean;

public class ReportHeader {
	private String handlerName;
	private String displayName;
	private String skin;
	private String gridObjectName;
	public String getHandlerName() {
		return handlerName;
	}
	public void setHandlerName(String handlerName) {
		this.handlerName = handlerName;
	}
	public String getDisplayName() {
		return displayName;
	}
	public void setDisplayName(String displayName) {
		this.displayName = displayName;
	}
	public String getSkin() {
		return skin;
	}
	public void setSkin(String skin) {
		this.skin = skin;
	}
	public String getGridObjectName() {
		return gridObjectName;
	}
	public void setGridObjectName(String gridObjectName) {
		this.gridObjectName = gridObjectName;
	}

}
